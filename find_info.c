#include "lem_in.h"

static int	is_info_same(t_room *rm1, t_room *rm2)
{
	if (rm1 && rm1 != rm2 && 
            (!ft_strcmp(rm1->name, rm2->name) || 
             (rm1->x == rm2->x && rm1->y == rm2->y)))
        return (1);
    return (0);
}
